def insert_dreams():
    """插入新的诗意梦境"""
    config = get_config()
    conn = yaspy.connect(user=config.user, password=config.password, dsn=config.dsn)
    cursor = conn.cursor()

    print("插入新的高质量诗意梦境到 YashanDB...")
    print("=" * 60)

    for i, content in enumerate(new_poetic_dreams, 1):
        try:
            # 插入梦境
            cursor.execute("""
                INSERT INTO dream_records (id, user_id, content, is_private)
                VALUES (seq_dream_records.NEXTVAL, :1, :2, :3)
            """, (1, content, 0))

            conn.commit()

            # 获取刚插入的记录ID
            cursor.execute("SELECT MAX(id) FROM dream_records WHERE user_id = 1")
            result = cursor.fetchone()
            dream_id = result[0]

            print(f"{i}. ✅ 插入成功 (ID: {dream_id})")
            print(f"   内容: {content[:40]}...")

        except Exception as e:
            print(f"{i}. ❌ 插入异常: {e}")
            conn.rollback()

    print("=" * 60)

    # 统计总数
    cursor.execute("SELECT COUNT(*) FROM dream_records")
    total = cursor.fetchone()[0]
    print(f"✨ 完成！数据库中共有 {total} 条梦境记录。")

    cursor.close()
    conn.close()

if __name__ == "__main__":
    import sys
    import io
